void set_layer_color(int layer) {
  for (int i = 0; i < RGB_MATRIX_LED_COUNT; i++) {
    HSV hsv = {
      .h = pgm_read_byte(&ledmap[layer][i][0]),
      .s = pgm_read_byte(&ledmap[layer][i][1]),
      .v = pgm_read_byte(&ledmap[layer][i][2]),
    };
    if (!hsv.h && !hsv.s && !hsv.v) {
        rgb_matrix_set_color( i, 0, 0, 0 );
    } else {
        RGB rgb = hsv_to_rgb( hsv );
        float f = (float)rgb_matrix_config.hsv.v / UINT8_MAX;
        rgb_matrix_set_color( i, f * rgb.r, f * rgb.g, f * rgb.b );
    }
  }
}

bool rgb_matrix_indicators_user(void) {
  if (rawhid_state.rgb_control) {
      return false;
  }
  if (keyboard_config.disable_layer_led) { return false; }
  switch (biton32(layer_state)) {
    case 1:
      set_layer_color(1);
      break;
    case 2:
      set_layer_color(2);
      break;
    case 3:
      set_layer_color(3);
      break;
   default:
    if (rgb_matrix_get_flags() == LED_FLAG_NONE)
      rgb_matrix_set_color_all(0, 0, 0);
    break;
  }
  return true;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    static bool aHeld = false;
    static bool dHeld = false;
    static bool wHeld = false;
    static bool sHeld = false;

    switch (keycode) {
      case KC_A:
        aHeld = record->event.pressed;
        if (dHeld && aHeld) {
          unregister_code(KC_D);
        } else if (dHeld && !aHeld){
          unregister_code(KC_A);
          register_code(KC_D);
          return false; // don't send original key pressed
        }
        return true;
      case KC_D:
        dHeld = record->event.pressed;
        if (aHeld && dHeld) {
          unregister_code(KC_A);
        } else if (aHeld && !dHeld){
          unregister_code(KC_D);
          register_code(KC_A);
          return false; // don't send original key pressed
        }
        return true;
      case KC_W:
        wHeld = record->event.pressed;
        if (sHeld && wHeld) {
          unregister_code(KC_S);
        } else if (sHeld && !wHeld){
          unregister_code(KC_W);
          register_code(KC_S);
          return false; // don't send original key pressed
        }
        return true;
      case KC_S:
        sHeld = record->event.pressed;
        if (wHeld && sHeld) {
          unregister_code(KC_W);
        } else if (wHeld && !sHeld){
          unregister_code(KC_S);
          register_code(KC_W);
          return false; // don't send original key pressed
        }
        return true;
      case ST_MACRO_0:
        if (record->event.pressed) {
          SEND_STRING(SS_TAP(X_V) SS_DELAY(100) SS_TAP(X_I) SS_DELAY(100) SS_TAP(X_W));
        }
        break;
      case RGB_SLD:
        if (record->event.pressed) {
          rgblight_mode(1);
        }
        return false;
    }
    return true;  // Process all other keycodes normally
}

typedef struct {
    bool is_press_action;
    uint8_t step;
} tap;

enum {
    SINGLE_TAP = 1,
    SINGLE_HOLD,
    DOUBLE_TAP,
    DOUBLE_HOLD,
    DOUBLE_SINGLE_TAP,
    MORE_TAPS
};

static tap dance_state[13];

uint8_t dance_step(tap_dance_state_t *state);

uint8_t dance_step(tap_dance_state_t *state) {
    if (state->count == 1) {
        if (state->interrupted || !state->pressed) return SINGLE_TAP;
        else return SINGLE_HOLD;
    } else if (state->count == 2) {
        if (state->interrupted) return DOUBLE_SINGLE_TAP;
        else if (state->pressed) return DOUBLE_HOLD;
        else return DOUBLE_TAP;
    }
    return MORE_TAPS;
}


